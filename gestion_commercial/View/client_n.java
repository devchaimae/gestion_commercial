    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/megecbase","root","");
            Statement stmt=con.createStatement();
            String requette="SELECT * FROM client where numcomp='"+jTextField1.getText()+"'";
            ResultSet rs = stmt.executeQuery(requette);
            while(rs.next()){
                //jTextField15.setText(rs.getString("contact"));
                jTextField4.setText(rs.getString("adresse"));
                jTextField17.setText(rs.getString("cp"));
                jTextField7.setText(rs.getString("numsiret"));
                jTextField8.setText(rs.getString("nfa"));
                jTextField9.setText(rs.getString("numidentif"));
                jTextField12.setText(rs.getString("telecopie"));
                jTextField13.setText(rs.getString("email"));
                jTextField14.setText(rs.getString("site"));
                jTextField16.setText(rs.getString("garantie"));
                jComboBox6.setSelectedItem(rs.getString("ville"));
                jComboBox1.setSelectedItem(rs.getString("pays"));
                jComboBox2.setSelectedItem(rs.getString("region"));
                jComboBox3.setSelectedItem(rs.getString("typeclient"));
                jComboBox5.setSelectedItem(rs.getString("delaipaiemt"));
                jComboBox4.setSelectedItem(rs.getString("modepaiemt"));
                jTextField19.setText(rs.getString("comptecollectif"));
                jTextField18.setText(rs.getString("quatite"));
                jTextField11.setText(rs.getString("telephone"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "On a pas trouvé la ligne corespondante");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

